def documentation_example(x: int, y: int) -> int:
    """
    This function demonstrates how to document a function using docstrings.

    Parameters:
    x (int): The first number to be added.
    y (int): The second number to be added.

    Returns:
    int: The sum of x and y.
    """
    return x + y


print(documentation_example(2,3))
